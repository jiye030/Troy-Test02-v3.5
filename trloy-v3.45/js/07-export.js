/* -------------------------------------------------
   Cursor Rules (.cursorrules)
   ------------------------------------------------- */
function buildCursorRules(data) {
  if (!data) return '';
  const vt = data?.visualTheme ?? {};
  const cp = data?.colorPalette ?? {};
  const tp = data?.typography ?? {};
  const cm = data?.components ?? {};
  const lo = data?.layout ?? {};
  const ru = data?.rules ?? {};
  const rs = data?.responsive ?? {};

  const lines = [];
  lines.push(`# .cursorrules — ${vt.title || 'TRLOY Design System'}`);
  lines.push(`# Generated by TRLOY · ${todayYMD()}`);
  lines.push('');
  lines.push('## Design Tokens');
  lines.push('');
  lines.push('### Colors (CSS Custom Properties)');
  lines.push('```css');
  lines.push(tokensToCSSVariables(data));
  lines.push('```');
  lines.push('');
  lines.push('### Typography');
  /* v3.45: tp.scale 배열을 우선 사용 — 8단계 전부 + letterSpacing/textTransform 포함
     scale이 없으면 기존 4단계 fallback */
  if (Array.isArray(tp.scale) && tp.scale.length) {
    tp.scale.forEach((s) => {
      const role = s.role || '';
      const family = s.family || '';
      const size = s.size || '';
      const weight = s.weight ?? '';
      const lh = s.lineHeight || '';
      const ls = s.letterSpacing || 'normal';
      const tt = s.textTransform || 'none';
      lines.push(
        `- ${role}: ${family}, ${size}, weight ${weight}, lh ${lh}, ` +
        `letterSpacing ${ls}, textTransform ${tt}`
      );
    });
  } else {
    ['display', 'heading', 'body', 'caption'].forEach((role) => {
      const s = tp[role];
      if (!s) return;
      lines.push(`- ${role}: ${s.family || ''}, ${s.size || ''}, weight ${s.weight || ''}, lh ${s.lineHeight || ''}`);
    });
  }
  lines.push('');
  lines.push('### Components');
  ['button', 'input', 'card'].forEach((name) => {
    const c = cm[name];
    if (!c) return;
    lines.push(`- ${name}: ${Object.entries(c).map(([k, v]) => `${k}=${v}`).join(', ')}`);
  });
  lines.push('');
  lines.push('### Layout');
  Object.entries(lo).forEach(([k, v]) => lines.push(`- ${k}: ${v}`));
  lines.push('');
  lines.push('### Responsive Breakpoints');
  (rs.breakpoints || []).forEach((bp) => {
    lines.push(`- ${bp.name || ''}: ${bp.query || ''} (${bp.cols || '?'}col)`);
  });
  lines.push('');
  lines.push("## Do's and Don'ts");
  (ru.dos ?? []).forEach((d) => lines.push(`- DO: ${d}`));
  (ru.donts ?? []).forEach((d) => lines.push(`- DON'T: ${d}`));
  lines.push('');
  lines.push('## Implementation Checklist');
  lines.push('- [ ] Declare color tokens as CSS :root variables');
  lines.push('- [ ] Use Google Fonts or system font fallback');
  lines.push('- [ ] Each section as independent component');
  lines.push('- [ ] Mobile responsive (breakpoint: 768px)');
  lines.push('- [ ] Intersection Observer + CSS transition for interactions');
  lines.push('- [ ] Semantic HTML (section, nav, header, footer)');
  lines.push('- [ ] Accessibility: alt, aria-label');

  return lines.join('\n');
}

